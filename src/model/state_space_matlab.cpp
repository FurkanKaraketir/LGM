QString matlabMatrix(const std::vector<QStringList>& rows) {
    if (rows.empty()) {
        return QStringLiteral("[]");
    }
    QStringList matlabRows;
    matlabRows.reserve(static_cast<int>(rows.size()));
    for (const QStringList& row : rows) {
        matlabRows.push_back(row.join(QStringLiteral(", ")));
    }
    return QStringLiteral("[%1]").arg(matlabRows.join(QStringLiteral("; ")));
}

void appendMatrixAssignment(QStringList& lines, const QString& name,
                            const std::vector<QStringList>& rows) {
    if (rows.empty()) {
        return;
    }
    lines.push_back(QStringLiteral("%1 = %2;").arg(name, matlabMatrix(rows)));
}

